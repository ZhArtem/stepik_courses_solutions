n = int(input())
l = [int(input()) for _ in range(n)]
m = int(input())
ans = '–ù–ï–¢'
for i in range(n):
  for j in range(i + 1, n):
    if l[i] * l[j] == m:
      ans = '–î–ê'
print(ans)