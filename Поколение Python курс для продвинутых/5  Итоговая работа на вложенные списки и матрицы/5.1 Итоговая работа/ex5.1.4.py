n = int(input())
matrix = [['.'] * n for _ in range(n)]
center = n // 2
for i in range(n):
    for j in range(n):
        if i == j or i == n - j - 1 or i == center or j == center:
            matrix[i][j] = '*'
        print(matrix[i][j], end=' ')
    print()

    