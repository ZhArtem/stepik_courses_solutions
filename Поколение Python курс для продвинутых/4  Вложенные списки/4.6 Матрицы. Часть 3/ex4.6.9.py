n, m = [int(i) for i in input().split()]
matrix = [[0] * m for _ in range(n)]
counter = 1
for k in range(n + m - 1):
  for i in range(n):
    for j in range(m):
      if i + j == k:
        matrix[i][j] = counter
        counter += 1
        break
for row in matrix:
  for dig in row:
    print(str(dig).ljust(3), end='')
  print()
  