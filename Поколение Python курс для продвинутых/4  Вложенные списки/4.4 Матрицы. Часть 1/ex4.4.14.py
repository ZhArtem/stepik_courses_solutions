# –ö–≤–∞–¥—Ä–∞—Ç–Ω–∞—è –º–∞—Ç—Ä–∏—Ü–∞ —Ä–∞–∑–±–∏–≤–∞–µ—Ç—Å—è –Ω–∞ —á–µ—Ç—ã—Ä–µ —á–µ—Ç–≤–µ—Ä—Ç–∏, –æ–≥—Ä–∞–Ω–∏—á–µ–Ω–Ω—ã–µ –≥–ª–∞–≤–Ω–æ–π –∏ –ø–æ–±–æ—á–Ω–æ–π –¥–∏–∞–≥–æ–Ω–∞–ª—è–º–∏: –≤–µ—Ä—Ö–Ω—é—é, –Ω–∏–∂–Ω—é—é, –ª–µ–≤—É—é –∏ –ø—Ä–∞–≤—É—é.



# –ù–∞–ø–∏—à–∏—Ç–µ –ø—Ä–æ–≥—Ä–∞–º–º—É, –∫–æ—Ç–æ—Ä–∞—è –≤—ã—á–∏—Å–ª—è–µ—Ç —Å—É–º–º—É —ç–ª–µ–º–µ–Ω—Ç–æ–≤: –≤–µ—Ä—Ö–Ω–µ–π —á–µ—Ç–≤–µ—Ä—Ç–∏; –ø—Ä–∞–≤–æ–π —á–µ—Ç–≤–µ—Ä—Ç–∏; –Ω–∏–∂–Ω–µ–π —á–µ—Ç–≤–µ—Ä—Ç–∏; –ª–µ–≤–æ–π —á–µ—Ç–≤–µ—Ä—Ç–∏.

# –§–æ—Ä–º–∞—Ç –≤—Ö–æ–¥–Ω—ã—Ö –¥–∞–Ω–Ω—ã—Ö
# –ù–∞ –≤—Ö–æ–¥ –ø—Ä–æ–≥—Ä–∞–º–º–µ –ø–æ–¥–∞—ë—Ç—Å—è –Ω–∞—Ç—É—Ä–∞–ª—å–Ω–æ–µ —á–∏—Å–ª–æ nn ‚Äî –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ —Å—Ç—Ä–æ–∫ –∏ —Å—Ç–æ–ª–±—Ü–æ–≤ –≤ –º–∞—Ç—Ä–∏—Ü–µ, –∑–∞—Ç–µ–º —ç–ª–µ–º–µ–Ω—Ç—ã –º–∞—Ç—Ä–∏—Ü—ã (—Ü–µ–ª—ã–µ —á–∏—Å–ª–∞) –ø–æ—Å—Ç—Ä–æ—á–Ω–æ —á–µ—Ä–µ–∑ –ø—Ä–æ–±–µ–ª.

# –§–æ—Ä–º–∞—Ç –≤—ã—Ö–æ–¥–Ω—ã—Ö –¥–∞–Ω–Ω—ã—Ö
# –ü—Ä–æ–≥—Ä–∞–º–º–∞ –¥–æ–ª–∂–Ω–∞ –≤—ã–≤–µ—Å—Ç–∏ —Ç–µ–∫—Å—Ç –≤ —Å–æ–æ—Ç–≤–µ—Ç—Å—Ç–≤–∏–∏ —Å —É—Å–ª–æ–≤–∏–µ–º –∑–∞–¥–∞—á–∏.

# –ü—Ä–∏–º–µ—á–∞–Ω–∏–µ. –≠–ª–µ–º–µ–Ω—Ç—ã –¥–∏–∞–≥–æ–Ω–∞–ª–µ–π –Ω–µ —É—á–∏—Ç—ã–≤–∞—é—Ç—Å—è.

# –¢–µ—Å—Ç–æ–≤—ã–µ –¥–∞–Ω–Ω—ã–µ üü¢
# Sample Input 1:

# 4
# 1 2 3 4
# 5 6 7 8
# 3 4 5 6
# 1 2 3 4
# Sample Output 1:

# –í–µ—Ä—Ö–Ω—è—è —á–µ—Ç–≤–µ—Ä—Ç—å: 5
# –ü—Ä–∞–≤–∞—è —á–µ—Ç–≤–µ—Ä—Ç—å: 14
# –ù–∏–∂–Ω—è—è —á–µ—Ç–≤–µ—Ä—Ç—å: 5
# –õ–µ–≤–∞—è —á–µ—Ç–≤–µ—Ä—Ç—å: 8
# Sample Input 2:

# 5
# 1 4 3 4 7
# 5 6 7 8 4
# 3 8 5 6 1
# 1 2 9 4 8
# 5 6 1 5 8
# Sample Output 2:

# –í–µ—Ä—Ö–Ω—è—è —á–µ—Ç–≤–µ—Ä—Ç—å: 18
# –ü—Ä–∞–≤–∞—è —á–µ—Ç–≤–µ—Ä—Ç—å: 19
# –ù–∏–∂–Ω—è—è —á–µ—Ç–≤–µ—Ä—Ç—å: 21
# –õ–µ–≤–∞—è —á–µ—Ç–≤–µ—Ä—Ç—å: 17
# Sample Input 3:

# 2
# 99 72
# 65 11
# Sample Output 3:

# –í–µ—Ä—Ö–Ω—è—è —á–µ—Ç–≤–µ—Ä—Ç—å: 0
# –ü—Ä–∞–≤–∞—è —á–µ—Ç–≤–µ—Ä—Ç—å: 0
# –ù–∏–∂–Ω—è—è —á–µ—Ç–≤–µ—Ä—Ç—å: 0
# –õ–µ–≤–∞—è —á–µ—Ç–≤–µ—Ä—Ç—å: 0


n = int(input())
matrix = [[int(i) for i in input().split()] for _ in range(n)]
s1, s2, s3, s4 = 0, 0, 0, 0
for i in range(n):
  for j in range(n):
    if i < j:
      if i < n - j - 1:
        s1 += matrix[i][j]
      elif i > n - j - 1:
        s2 += matrix[i][j]
    elif i > j:
      if i > n - j - 1:
        s3 += matrix[i][j]
      elif i < n - j - 1:
        s4 += matrix[i][j]
print(f'–í–µ—Ä—Ö–Ω—è—è —á–µ—Ç–≤–µ—Ä—Ç—å: {s1}')
print(f'–ü—Ä–∞–≤–∞—è —á–µ—Ç–≤–µ—Ä—Ç—å: {s2}')
print(f'–ù–∏–∂–Ω—è—è —á–µ—Ç–≤–µ—Ä—Ç—å: {s3}')
print(f'–õ–µ–≤–∞—è —á–µ—Ç–≤–µ—Ä—Ç—å: {s4}')        