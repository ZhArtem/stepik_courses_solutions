n = int(input())
l = [[int('1') for j in range(1, i + 1)] for i in range(1, n + 2)]
for i in range(2, n + 1):
  for j in range(1, i):
    l[i][j] = l[i - 1][j - 1] + l[i - 1][j]
print(l[n])
