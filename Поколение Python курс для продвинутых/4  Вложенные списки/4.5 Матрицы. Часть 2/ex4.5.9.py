n = int(input())
matrix = [[int(i) for i in input().split()] for _ in range(n)]

def ismagic(matrix):
  all = []
  total = sum(matrix[0])
  for row in matrix:
    if sum(row) != total:
      return False
    all.extend(row)
  for i in range(1, n*n + 1):
    if i not in all:
      return False
  counter1, counter2, counter3 = 0, 0, 0
  for i in range(n):
    counter3 = 0
    counter1 += matrix[i][i]
    counter2 += matrix[i][n - i - 1]
    for j in range(n):
      counter3 += matrix[i][j]
    if counter3 != total:
      return False
  if counter1 != total or counter2 != total:
    return False
  return True

print('YES' if ismagic(matrix) else 'NO')
    
