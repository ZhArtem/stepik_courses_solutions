n = int(input())
matrix = [input().split() for _ in range(n)]
matrix2 = [['0'] * n for _ in range(n)]
for i in range(n):
  for j in range(n):
    matrix2[j][-i - 1] = matrix[i][j]
for row in matrix2:
  print(*row)
  