d = {
    1: "AEILNORSTU",
    2: "DG",
    3: "BCMP",
    4: "FHVWY",
    5: "K",
    8: "JX",
    10: "QZ"
}
score = 0
word = input()
for c in word:
    for k, v in d.items():
        if c in v:
            score += k
print(score)            
