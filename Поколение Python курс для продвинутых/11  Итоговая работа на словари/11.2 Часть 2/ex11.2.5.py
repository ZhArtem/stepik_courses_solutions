# –ü—Ä–∏–º–µ—á–∞–Ω–∏–µ 3. –°–ª–∏—è–Ω–∏–µ –ø—É—Å—Ç—ã—Ö —Å–ª–æ–≤–∞—Ä–µ–π –¥–æ–ª–∂–Ω–æ –±—ã—Ç—å –ø—É—Å—Ç—ã–º —Å–ª–æ–≤–∞—Ä–µ–º.


def merge(values):      # values - —ç—Ç–æ —Å–ø–∏—Å–æ–∫ —Å–ª–æ–≤–∞—Ä–µ–π
    res = {}
    for dic in values:
        for k, v in dic.items():
            if k not in res:
                res[k] = set()
                res[k].add(v)
            else:
                res[k].add(v)
    return res

    