keyboard = {
    '.,?!:': '1',
    'ABC': '2',
    'DEF': '3',
    'GHI': '4',
    'JKL': '5',
    'MNO': '6',
    'PQRS': '7',
    'TUV': '8',
    'WXYZ': '9',
    ' ': '0'
}
for c in input().upper():
    for key, value in keyboard.items():
        if c in key:
            print(value * (key.find(c) + 1), end='')
            break
