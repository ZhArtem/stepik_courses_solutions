word1, word2 = input(), input()
d1, d2 = {}, {}
for c in word1:
  d1[c] = d1.setdefault(c, 0) + 1
for c in word2:
  d2[c] = d2.setdefault(c, 0) + 1
print('YES') if d1 == d2 else print('NO')

