# –§–æ—Ä–º–∞—Ç –≤—Ö–æ–¥–Ω—ã—Ö –¥–∞–Ω–Ω—ã—Ö
# –ù–∞ –≤—Ö–æ–¥ –ø—Ä–æ–≥—Ä–∞–º–º–µ –ø–æ–¥–∞—é—Ç—Å—è –¥–≤–∞ –ø—Ä–µ–¥–ª–æ–∂–µ–Ω–∏—è, –∫–∞–∂–¥–æ–µ –Ω–∞ –æ—Ç–¥–µ–ª—å–Ω–æ–π —Å—Ç—Ä–æ–∫–µ.

# –§–æ—Ä–º–∞—Ç –≤—ã—Ö–æ–¥–Ω—ã—Ö –¥–∞–Ω–Ω—ã—Ö
# –ü—Ä–æ–≥—Ä–∞–º–º–∞ –¥–æ–ª–∂–Ω–∞ –≤—ã–≤–µ—Å—Ç–∏ YES , –µ—Å–ª–∏ –ø—Ä–µ–¥–ª–æ–∂–µ–Ω–∏—è ‚Äì –∞–Ω–∞–≥—Ä–∞–º–º—ã –∏ NO –≤ –ø—Ä–æ—Ç–∏–≤–Ω–æ–º —Å–ª—É—á–∞–µ.

# –ü—Ä–∏–º–µ—á–∞–Ω–∏–µ. –ö—Ä–æ–º–µ —Å–ª–æ–≤ –≤ —Ç–µ–∫—Å—Ç–µ –º–æ–≥—É—Ç –ø—Ä–∏—Å—É—Ç—Å—Ç–≤–æ–≤–∞—Ç—å –ø—Ä–æ–±–µ–ª—ã –∏ –∑–Ω–∞–∫–∏ –ø—Ä–µ–ø–∏–Ω–∞–Ω–∏—è .,!?:;-. –î—Ä—É–≥–∏—Ö —Å–∏–º–≤–æ–ª–æ–≤ –≤ —Ç–µ–∫—Å—Ç–µ –Ω–µ—Ç.

# –¢–µ—Å—Ç–æ–≤—ã–µ –¥–∞–Ω–Ω—ã–µ üü¢
# Sample Input 1:

# –í–∏–∂—É –∑–≤–µ—Ä–µ–π
# –ñ–∏–≤—É —Ä–µ–∑–≤–µ–π
# Sample Output 1:

# YES
# Sample Input 2:

# –ö–æ–≥–¥–∞ —É–≤–∏–¥–∏–º—Å—è
# —Ç–æ–≥–¥–∞ –∏ —Å–≤–∏–¥–∏–º—Å—è
# Sample Output 2:

# NO
# Sample Input 3:

# –° –º–∞—è –≤–µ—Å–Ω–æ–π
# —Å–∞–º —è –Ω–µ —Å–≤–æ–π
# Sample Output 3:

# YES


s1 = input().split()
s2 = input().split()
d1, d2 = {}, {}
for w in s1:
  w = w.strip('.,!?:;-').lower()
  for c in w:
    d1[c] = d1.setdefault(c, 0) + 1
for w in s2:
  w = w.strip('.,!?:;-').lower()
  for c in w:
    d2[c] = d2.setdefault(c, 0) + 1
print('YES' if d1 == d2 else 'NO')

